'use client';

import { useState, useEffect } from 'react';
import Link from 'next/link';
import { cn } from '@/lib/utils';
import { X } from 'lucide-react';

export function ExitPopup() {
    const [isVisible, setIsVisible] = useState(false);
    const [hasTriggered, setHasTriggered] = useState(false);

    useEffect(() => {
        const handleMouseLeave = (e: MouseEvent) => {
            // Trigger only if the mouse goes out of the window at the top (indicating intent to close tab/window)
            if (e.clientY <= 0 && !hasTriggered) {
                setIsVisible(true);
                setHasTriggered(true); // Ensure it only triggers once per session
            }
        };

        document.addEventListener('mouseleave', handleMouseLeave);

        return () => {
            document.removeEventListener('mouseleave', handleMouseLeave);
        };
    }, [hasTriggered]);

    const whatsappNumber = "5511978519899";
    const whatsappMessage = "Olá, gostaria de aproveitar a condição especial do Jardins da Hípica.";
    const whatsappUrl = `https://wa.me/${whatsappNumber}?text=${encodeURIComponent(whatsappMessage)}`;

    if (!isVisible) return null;

    return (
        <div className="fixed inset-0 z-[100] flex items-center justify-center bg-black/60 backdrop-blur-sm p-4">
            <div
                className="relative w-full max-w-lg bg-white rounded-3xl shadow-2xl overflow-hidden animate-in fade-in zoom-in duration-300"
                onClick={(e) => e.stopPropagation()}
            >
                <button
                    onClick={() => setIsVisible(false)}
                    className="absolute top-4 right-4 z-10 w-8 h-8 flex items-center justify-center bg-white/50 hover:bg-white rounded-full text-gray-500 hover:text-gray-900 transition-colors backdrop-blur-sm"
                    aria-label="Fechar"
                >
                    <X className="w-5 h-5" />
                </button>

                <div className="bg-primary/5 p-8 text-center pt-12">
                    <h2 className="text-3xl font-serif font-bold text-primary mb-4">
                        Espere! Não vá embora ainda...
                    </h2>
                    <p className="text-gray-600 mb-8 max-w-sm mx-auto">
                        Temos uma <strong>condição exclusiva e por tempo limitado</strong> para você que acessou nosso site hoje.
                    </p>

                    <Link
                        href={whatsappUrl}
                        target="_blank"
                        rel="noopener noreferrer"
                        onClick={() => setIsVisible(false)}
                        className="inline-flex items-center justify-center w-full bg-[#25D366] text-white font-bold py-4 px-8 rounded-xl shadow-lg hover:shadow-xl hover:bg-[#20bd5a] transition-all duration-300 text-lg group"
                    >
                        <svg
                            xmlns="http://www.w3.org/2000/svg"
                            width="24"
                            height="24"
                            viewBox="0 0 24 24"
                            fill="currentColor"
                            className="mr-3 group-hover:scale-110 transition-transform"
                        >
                            <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413Z" />
                        </svg>
                        Falar com Consultor Agora
                    </Link>

                    <button
                        onClick={() => setIsVisible(false)}
                        className="mt-6 text-sm text-gray-400 hover:text-gray-600 underline font-light"
                    >
                        Não tenho interesse, quero apenas fechar.
                    </button>
                </div>
            </div>
        </div>
    );
}
